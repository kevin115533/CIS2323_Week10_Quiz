package FormLetterWriter;

public class FormLetterWriter{
	public void displaySaluation(String x){
		System.out.println("Dear Mr. " + x + ", Thank you for the recent order");
		}
		
	public void displaySaluation(String y, String x){
		System.out.println("Dear " + y + " " + x + ", Thank you for the recent order");
		}	
		
	
}